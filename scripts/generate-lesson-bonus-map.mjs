// Builds curriculum/lesson-bonus-activities.js from lesson config.json files.
// Each lesson with practice.optionalActivity gets a curriculum-hub dropdown entry
// that deep-links to /lessons/<id>/?extra=activity.
import { readFileSync, writeFileSync, readdirSync, existsSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, "..");
const lessonsDir = join(root, "lessons");
const outFile = join(root, "curriculum", "lesson-bonus-activities.js");

const LESSON_DIR_RE = /^(\d+)-(\d+)(-flagship)?$/;

const entries = readdirSync(lessonsDir)
  .filter((d) => LESSON_DIR_RE.test(d))
  .filter((d) => existsSync(join(lessonsDir, d, "config.json")))
  .map((id) => {
    const cfg = JSON.parse(
      readFileSync(join(lessonsDir, id, "config.json"), "utf8"),
    );
    const act = cfg.practice?.optionalActivity;
    if (!act?.name) return null;
    const emoji = act.emoji || "🎯";
    return [
      id,
      {
        text: `${emoji} ${act.name}`,
        href: `/lessons/${id}/?extra=activity`,
        isBonus: true,
      },
    ];
  })
  .filter(Boolean)
  .sort((a, b) => {
    const ma = a[0].match(LESSON_DIR_RE);
    const mb = b[0].match(LESSON_DIR_RE);
    return (
      Number(ma[1]) - Number(mb[1]) ||
      Number(ma[2]) - Number(mb[2]) ||
      (a[0].endsWith("-flagship") ? 1 : 0) - (b[0].endsWith("-flagship") ? 1 : 0)
    );
  });

const map = Object.fromEntries(entries);
const mapLiteral = JSON.stringify(map, null, 2);

// Standalone file (optional reference / debugging).
const js = `// Auto-generated by scripts/generate-lesson-bonus-map.mjs — do not edit by hand.
// Regenerate: npm run generate-lesson-bonus-map
window.LESSON_BONUS_ACTIVITIES = ${mapLiteral};
`;
writeFileSync(outFile, js, "utf8");

// Inline into curriculum/index.html so the hub dropdown never depends on a
// second script request (avoids 404s from relative-path or cache issues).
const hubFile = join(root, "curriculum", "index.html");
const hub = readFileSync(hubFile, "utf8");
const begin = "        // BEGIN_LESSON_BONUS_MAP";
const end = "        // END_LESSON_BONUS_MAP";
const start = hub.indexOf(begin);
const stop = hub.indexOf(end);
if (start === -1 || stop === -1 || stop <= start) {
  throw new Error(
    "curriculum/index.html is missing BEGIN_LESSON_BONUS_MAP / END_LESSON_BONUS_MAP markers",
  );
}
const injected =
  begin +
  " (auto — npm run generate-lesson-bonus-map)\n" +
  `        var LESSON_BONUS_ACTIVITIES = ${mapLiteral};\n` +
  "        " +
  end;
const patched = hub.slice(0, start) + injected + hub.slice(stop + end.length);
writeFileSync(hubFile, patched, "utf8");

console.log(
  `Wrote ${entries.length} bonus activities to ${outFile} and inlined into curriculum/index.html`,
);
