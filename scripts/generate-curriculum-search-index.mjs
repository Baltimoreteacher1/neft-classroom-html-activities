#!/usr/bin/env node
/**
 * Build-time MiniSearch index for the curriculum hub.
 * Source: data/curriculum-manifest.json (+ optional bonus map).
 * Output: data/curriculum-search-index.json (MiniSearch.loadJSON compatible).
 *
 *   node scripts/generate-curriculum-search-index.mjs
 */
import { readFileSync, writeFileSync, existsSync } from "fs";
import { resolve, dirname } from "path";
import { fileURLToPath } from "url";
import MiniSearch from "minisearch";

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = resolve(__dirname, "..");
const manifestPath = resolve(root, "data/curriculum-manifest.json");
const outPath = resolve(root, "data/curriculum-search-index.json");

if (!existsSync(manifestPath)) {
  console.error("Missing data/curriculum-manifest.json — run npm run generate-curriculum-manifest");
  process.exit(1);
}

const manifest = JSON.parse(readFileSync(manifestPath, "utf8"));
const lessons = manifest.lessons || [];

/** Flatten resource labels from manifest.resources object. */
function resourceLabels(resources) {
  if (!resources || typeof resources !== "object") return "";
  return Object.keys(resources)
    .filter((k) => resources[k]?.exists !== false)
    .join(" ");
}

const docs = lessons.map((l) => {
  const id = l.id || `${l.unit}-${l.lesson}`;
  return {
    id,
    unit: String(l.unit ?? ""),
    unitLabel: `Unit ${l.unit}`,
    lesson: String(l.lesson ?? ""),
    title: l.title || "",
    standard: l.standard || "",
    objective: l.objective || "",
    languageObjective: l.languageObjective || "",
    topic: l.topic || "",
    lessonPath: l.lessonPath || `/lessons/${id}/`,
    resources: resourceLabels(l.resources),
    searchText: [
      l.title,
      l.standard,
      l.objective,
      l.languageObjective,
      l.topic,
      resourceLabels(l.resources),
      `lesson ${id}`,
      `unit ${l.unit}`,
    ]
      .filter(Boolean)
      .join(" "),
  };
});

const miniSearch = new MiniSearch({
  fields: ["title", "standard", "objective", "languageObjective", "topic", "resources", "searchText"],
  storeFields: [
    "id",
    "unit",
    "unitLabel",
    "lesson",
    "title",
    "standard",
    "objective",
    "lessonPath",
  ],
  searchOptions: {
    boost: { title: 3, standard: 2, objective: 1.5 },
    fuzzy: 0.2,
    prefix: true,
  },
});

miniSearch.addAll(docs);

const payload = {
  note: "GENERATED by scripts/generate-curriculum-search-index.mjs — do not hand-edit.",
  schemaVersion: 1,
  generatedAt: new Date().toISOString(),
  documentCount: docs.length,
  index: miniSearch.toJSON(),
};

writeFileSync(outPath, JSON.stringify(payload, null, 2) + "\n");
console.log(`Wrote ${docs.length} documents to ${outPath}`);
